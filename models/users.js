const mongoose = require('mongoose');
const { lazy } = require('react');
const users =  new mongoose.Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
}); 

const userData = mongoose.model('User', users);
module.exports = userData;